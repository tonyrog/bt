to_string(ID) when ID band (bnot 16#ffff) =:= 0 ->
    maps:get(ID, uuid_map(), undefined).

from_string(String) when is_list(String) ->
    NString = string:join(string:tokens(string:to_lower(String), " "), " "),
    maps:get(NString, str_map(), undefined).

